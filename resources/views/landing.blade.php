                    <h3 class="section-subheading text-align">Sejak diberlakukannya Struktur Organisasi Pemerintah Daerah (SOPD) baru di lingkungan Pemerintah Kabupaten Bengkalis terhitung 1 Januari 2017, setidaknya ada 5 SOPD yang hilang. Juga terjadi perubahan nama dinas maupun badan, ada yang dimerjer, ada pula muncul SOPD baru, yang pisah atau berdiri sendiri.
                    Salah satu adalah Dinas Komunikasi, Informatika dan Statistik (Diskominfotik). Dulunya urusan komunikasi dan informasi ini berada satu atap dengan Dinas Perhubungan. Namun sesuai amanah Peraturan Pemerintah Nomor 18 Tahun 2016 tentang Perangkat Daerah dan Peraturan Menteri Komunikasi dan Informatika Nomor 14 Tahun 2016 tentang Pedoman Nomenklatur Perangkat Daerah Bidang Komunikasi dan Informatika, diperkuat Peraturan Bupati Bengkalis Nomor 51 Tahun 2016 tentang Kedudukan, Susunan Organisasi, Eselonering, Tugas, Fungsi dan Uraian Tugas serta Tata Kerja, Diskominfotik kini berdiri sendiri dan berkantor di gedung eks Dinas Pasar dan Kebersihan.</h3>
                    <h3 class="section-subheading text-align">Seperti diketahui, Dinas Pasar dan Kebersihan melebur dengan Dinas Perdagangan dan Perindustrian untuk urusan pasar, sedangkan urusan kebersihan bergabung ke Dinas Lingkungan Hidup.
                        Diskominfotik memiliki tugas dan tupoksi yang lebih luas cakupannya dibandingkan sebelumnya. Karena selain ditambah urusan statistik, Bagian Pengelolaan Data Elektronik (PDE) yang dulunya berada di Sekretariat Daerah, kini menjadi bagian dari Diskominfotik, termasuk urusan publikasi, kerjasama media urusan public relation yang dulunya ditangani Bagian Humas Sekretariat Daerah.</h3>
                    <h3 class="section-subheading text-align">Seiring berjalannya waktu, tahun 2018 terjadi perubahan dalam susunan organisasi Diskominfotik, Layanan Pengadaan Secara Elektronik (LPSE) tidak lagi menjadi salah satu tugas dan fungsi Diskominfotik, pindah ke Bagian Pengadaan Barang/Jasa Sekretariat Daerah. Perubahan ini didasarkan Peraturan Bupati Nomor 58 Tahun 2018 tentang Perubahan atas Peratura Bupati Bengkalis Nomor 51 tahun 2016 tentang Kedudukan, Susunan Organisasi, Eselonering, Tugas, Fungsi dan Uraian Tugas serta Tata Kerja.</h3>
